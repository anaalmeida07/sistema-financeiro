    <h1 class="h1-sobre">Nossa história</h1>
    <p>Três estudantes de Análise e Desenvolvimento de Sistemas, Ana Carla, Beatriz Feitosa e Luiz Henrique Menezes, se reuniram com um propósito em mente: criar um sistema financeiro revolucionário para seu trabalho de conclusão de curso.</p>
    <p>Ana Carla, a líder do grupo, tinha uma visão clara: queria desenvolver uma solução que tornasse a gestão financeira pessoal mais acessível e intuitiva para todos. Beatriz Feitosa, apaixonada por gatos, sugeriu que o sistema fosse batizado de "Neneko", inspirado na palavra japonesa para "gato", que também se tornou a inspiração para o logotipo do projeto. Já Luiz Henrique Menezes, com sua habilidade técnica afiada, estava determinado a fazer do Neneko um sucesso.</p>
    <p>Combinando suas habilidades e motivações, o trio mergulhou de cabeça no projeto. Eles queriam criar algo que pudesse substituir os antigos caderninhos e as complicadas planilhas de Excel que muitas pessoas usavam para controlar suas finanças.</p>
    <p>Depois de meses de trabalho árduo e muitas noites sem dormir, o Neneko estava pronto. Era uma plataforma online elegante e intuitiva, projetada para simplificar a gestão financeira pessoal. Com recursos como categorização automática de despesas, tabela de gastos e visualizações gráficas simples.</p>
